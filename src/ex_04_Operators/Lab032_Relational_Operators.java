package ex_04_Operators;

public class Lab032_Relational_Operators {
    public static void main(String[] args) {

//        <    -> Less Than
//        < =  -> Less than or equal to
//        >    -> Grater than
//        > =  -> Grater or equal
//        ==   -> Equal to (but checking)
//        ! =  -> Not equal

        int a = 10;
        int b = 20;

        boolean c = a > b;
        System.out.println(c);

        int d = 30;
        int e = 40;

        boolean g = e > d;
        System.out.println(g);


    }
}
